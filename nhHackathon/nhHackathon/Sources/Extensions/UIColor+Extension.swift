import UIKit

extension UIColor {
    
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 189.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 38.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var salmon: UIColor {
        return UIColor(red: 1.0, green: 139.0 / 255.0, blue: 118.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var aquaMarine: UIColor {
        return UIColor(red: 74.0 / 255.0, green: 205.0 / 255.0, blue: 189.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var greyishBrown: UIColor {
        return UIColor(white: 79.0 / 255.0, alpha: 1.0)
    }
    
}
